export function buildCoverLetterPrompt(
  resume: string,
  jobDescription: string,
): string {
  return `你是一位资深求职顾问，擅长根据候选人简历与目标岗位 JD 撰写专业、真诚的求职信（Cover Letter）。

请根据以下简历与岗位描述，生成一封中文求职信，以 JSON 格式返回（不要包含 markdown 代码块）：

{
  "fullText": "完整求职信，含称呼、正文段落、结尾敬语与署名占位（如「求职者：XXX」）。约 300-500 字，分 3-4 段",
  "highlights": ["本求职信突出的与岗位匹配的亮点，3-5 条短句"]
}

写作要求：
1. 开头表明求职意向与岗位名称（从 JD 中提取）
2. 正文结合简历中的经历与成果，说明为何匹配该岗位，引用具体项目/技能，避免空泛
3. 体现对公司的了解意愿与加入动机（可基于 JD 合理推断）
4. 语气专业、真诚，第一人称，不要夸张或虚假承诺
5. 不要重复粘贴整段简历，应提炼与 JD 最相关的 2-3 段经历
6. 若简历中无姓名，署名处用「求职者」即可

岗位 JD：
---
${jobDescription}
---

候选人简历：
---
${resume}
---`;
}
